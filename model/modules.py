class LengthRegulator_Discriminator(nn.Module):
    """ Length Regulator """
    def __init__(self, model_config):
        super(LengthRegulator_Discriminator, self).__init__()
        max_pos = model_config["max_seq_len"]
        hidden_size = model_config["variance_predictor"]["filter_size"]
        self.position_enc = nn.Parameter(
            get_sinusoid_encoding_table(max_pos+1, hidden_size), requires_grad=False)

    def LR(self, x, duration, max_len):
        output = list()
        position = list()
        mel_len = list()
        for batch, expand_target in zip(x, duration):
            expanded, pos = self.expand(batch, expand_target)
            output.append(expanded)
            position.append(pos)
            mel_len.append(expanded.shape[0])

        if max_len is not None:
            output = pad(output, max_len)
            position = pad(position, max_len)
        else:
            output = pad(output)
            position = pad(position)
        return output, position, torch.LongTensor(mel_len).cuda()

    def expand(self, batch, predicted):
        out = list()
        pos = list()
        for i, vec in enumerate(batch):
            expand_size = predicted[i].item()
            out.append(vec.expand(int(expand_size), -1))
            pos.append(self.position_enc[:int(expand_size), :])
        out = torch.cat(out, 0)
        pos = torch.cat(pos, 0)
        return out, pos

    def forward(self, x, duration, max_len=None):
        output, position, mel_len = self.LR(x, duration, max_len)
        return output, position, mel_len
